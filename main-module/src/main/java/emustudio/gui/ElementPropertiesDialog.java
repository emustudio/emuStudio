    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel)tblSettings.getModel();
        model.setRowCount(0);
        
        for (Map.Entry<String, String> entry : settings.entrySet()) {
            model.addRow(new String[] { entry.getKey(), entry.getValue() });
        }
    }
